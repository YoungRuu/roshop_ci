<h2 class="g-title">Chính sách giao nhận
</h2>
<div class="g-list">
  Khi bạn đặt hàng thành công, nhân viên chăm sóc khách hàng của MAZA sẽ xác nhận đơn hàng của bạn qua điện thoại. Sau khi bạn xác nhận, MAZA sẽ tiến hành giao hàng cho bạn.Trước khi hàng được giao đến địa chỉ bạn yên cầu, nhân viên bưu điện sẽ liên lạc cho bạn trước để bạn tiện sắp xếp thời gian. Thời gian bạn nhận được hàng:
  <div>
    <br>
  </div>
  <div>-	Nếu bạn ở TP.HCM. Thời gian nhận hàng: 1 – 2 ngày.
  </div>
  <div>
    <br>
  </div>
  <div>-	Bạn ở khu vực 1: An Giang, Bình Dương, Bình Phước, Bà Rịa – Vũng Tàu, Bạc Liêu, Bến Tre, Bình Thuận, Cà Mau, Cần Thơ, Đồng Nai, Đồng Tháp, Hậu Giang, Kiên Giang, Khánh Hoà, Long An, Lâm Đồng, Ninh Thuận, Sóc Trăng, Tây Ninh, Tiền Giang, Trà Vinh, Vĩnh Long. Thời gian nhận hàng: 2 - 3 ngày.
  </div>
  <div>
    <br>
  </div>
  <div>-	Bạn ở khu vực 2: Bình Định, Đà Nẵng, Huế, Gia Lai, Kon Tum, Phú Yên, Quảng Trị, Quảng Nam, Quảng Ngãi, Đắc Lắc, Đắc Nông. Thời gian nhận hàng: 3 – 5 ngày.-	Bạn ở khu vực 3: Bắc Cạn, Bắc Giang, Bắc Ninh, Cao Bằng, Điện Biên, Hà Nội, Hà Giang, Hà Nam, Hà Tĩnh, Hải Dương, Hải Phòng, Hưng Yên, Hoà Bình, Lào Cai, Lai Châu, Lạng Sơn, Nam Định, Nghệ An, Ninh Bình, Phú Thọ, Quảng Ninh, Quảng Bình, Sơn La, Thái Bình, Thái Nguyên, Tuyên Quang, Vĩnh Thúc, Thanh Hoá, Yên Bái. Thời gian nhận hàng: 5- 7 ngày.
  </div>
  <div>
    <br>
  </div>
  <div>Thời gian nhận hàng thực tế có thể thay đổi so với thời gian nhận hàng dự kiến tuỳ vào quá trình chuyển hàng của Bưu Điện.
  </div>        
</div>
